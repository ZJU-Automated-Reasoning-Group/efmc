        self._var_at_time_cache = {}
        self._substitution_cache = {}

        self.use_bv = system.has_bv
        self.use_int = system.has_int
        self.use_real = system.has_real
        self.use_bool = system.has_bool
        if self.use_bv:
            self.bv_size = system.variables[0].sort().size()

