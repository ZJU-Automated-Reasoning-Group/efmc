    def _create_var_at_time(self, var: z3.ExprRef, t: int) -> z3.ExprRef:
        """Creates a variable at a specific time point with caching"""
        key = (str(var), t)
        if key not in self._var_at_time_cache:
            name = f"{str(var)}@{t}"
            if self.use_bv:
                result = z3.BitVec(name, self.bv_size)
            elif self.use_int:
                result = z3.Int(name)
            elif self.use_real:
                result = z3.Real(name)
            elif self.use_bool:
                result = z3.Bool(name)
            else:
                raise NotImplementedError("Unsupported variable type")
            self._var_at_time_cache[key] = result
        return self._var_at_time_cache[key]

